static Paragraph P(string text, bool bold = false, int? sizeHalfPoints = null, JustificationValues? justify = null)
{
    var run = new Run();
    var rPr = new RunProperties();
    if (bold) rPr.AppendChild(new Bold());
    if (sizeHalfPoints.HasValue) rPr.AppendChild(new FontSize { Val = sizeHalfPoints.Value.ToString() });
    run.AppendChild(rPr);
    run.AppendChild(new Text(text) { Space = SpaceProcessingModeValues.Preserve });

    var pPr = new ParagraphProperties();
    if (justify.HasValue) pPr.AppendChild(new Justification { Val = justify });
    var p = new Paragraph(pPr);
    p.AppendChild(run);
    return p;
}

static Paragraph Heading(string text, int level)
{
    var size = level == 1 ? 32 : level == 2 ? 28 : 24;
    var p = P(text, bold: true, sizeHalfPoints: size);
    p.ParagraphProperties ??= new ParagraphProperties();
    p.ParagraphProperties.SpacingBetweenLines = new SpacingBetweenLines { Before = "240", After = "120" };
    return p;
}

